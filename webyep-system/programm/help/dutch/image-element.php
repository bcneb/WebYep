<h3>Omschrijving</h3>
<p>In het &quot;Bewerk Afbeelding&quot; venster kunt u afbeeldingen (zoals foto's of illustraties)
opnemen in uw website of ze weer verwijderen.</p>
<p><b>Belangrijk:</b> De afbeelding moet zijn opgeslagen als een &quot;internet bruikbaar&quot; bestandsformaat
  - hetgeen betekent:</p>
<ul>
  <li>De afbeelding moet klein zijn in omvang (max. 100-150 kBytes) en in dimensies (niet groter dan 200 x 300 pixels).</li>
  <li>Het bestandsformaat moet ondersteund worden door webbrowsers (zoals GIF of JPEG).</li>
</ul>
<h3>Gebruik</h3>
<p><b>Wijzig Afbeelding:</b></p>
<p>Klik op de knop &quot;Selecteer...&quot; om de afbeelding te selecteren die u op de website wilt invoegen (uploaden). Selecteer het bestand in het dialoogvenster en klik &quot;OK&quot; (of &quot;Open&quot;). Klik vervolgens op &quot;Bewaar&quot; om het bestand naar de server te kopi&euml;ren.</p>
<p><i>Opmerking:</i> Het uploaden van een bestand van uw computer naar de server (waar uw website zich bevindt) kan enige tijd kosten, afhankelijk van de snelheid van uw internetaansluiting.</p>
<p><b>Afbeelding verwijderen:</b></p>
<p>Klik op de knop &quot;Verwijder&quot; om de afbeelding te verwijderen van de pagina en van de webserver.</p>
<p>Als u vervolgens op &quot;Bewaar&quot; of op &quot;Verwijder&quot; klikt, zal het bewerkingsvenster sluiten en wordt de gewijzigde pagina getoond.<br>
  <span class="remark">In sommmige uitzonderlijke gevallen kan het nodig zijn om de pagina in uw brwoser te verversen.</span></p>
